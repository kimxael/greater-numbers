class SqurArr{
    public static void main(String[] args) {
        int n[] = new int[10];   
        n[0] = 1;    
        n[1] = 4;    
        n[2] = 9;    
        n[3] = 16;   
        n[4] = 25;   
        n[5] = 36;  
        n[6] = 49;  
        n[7] = 64;   
        n[8] = 81;   
        n[9] = 100;  
        System.out.println("Squares of numbers 1 to 10:");
        for (int i = 0; i < 10; i++) {
            System.out.println(n[i]);
        }
    }
}